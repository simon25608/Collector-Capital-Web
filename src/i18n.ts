const getBrowserLanguage = () => {
  const browserLang = navigator.language || (navigator as any).userLanguage;
  if (!browserLang) return "en";

  const shortLang = browserLang.split("-")[0].toLowerCase();
  return shortLang === "es" ? "es" : "en";
};

